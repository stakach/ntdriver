    ULONG Prepared;
    ULONG Powered;
    ULONG InterfaceCreated;

    ULONG Answer;
    ULONG SeenByDriver;
    ULONG DeviceSeenByDriver;
    ULONG RuntimeValue;
    ULONG CustomPropertyValue;

